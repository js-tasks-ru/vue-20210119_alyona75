<template>
  <div id="app" style="width: 250px; margin: 2rem auto">
    <div style="display: flex; justify-content: space-between">
      <button
        v-for="i in 5"
        :key="i"
        @click="page = i"
        :class="{ active: page === i }"
      >
        {{ i }}
      </button>
    </div>

    <hr />

    <app-pagination :page="page" :per-page="5" :items="cards">
      <template v-slot:default="slots">
        <fieldset :key="slots.item.id">
          <legend>Card #{{ slots.item.id }}</legend>
          {{ slots.item.text }}
        </fieldset>
      </template>
    </app-pagination>
  </div>
</template>

<script>
import AppPagination from './components/AppPagination';

export default {
  name: 'App',
  components: { AppPagination },

  data() {
    return {
      page: 1,
      cards: Array.from(Array(22), (_, i) => ({
        id: i + 1,
        text: 'Body',
      })),
    };
  },
};
</script>

